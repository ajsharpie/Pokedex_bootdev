func GetPokemon(url string, c *Cache) (Pokemon, error) {
	if result, ok := c.Get(url); ok {
		var cachedData Pokemon
		err := json.Unmarshal(result, &cachedData)
		if err != nil {
			fmt.Println("Error unmarshaling cached data")
			return Pokemon{}, err
		}
		fmt.Println(cachedData.Name)
		return cachedData, nil
	} else {
		resp, err := http.Get(url)
		if err != nil {
			fmt.Println("Error fetching data")
			return Pokemon{}, err
		}
		defer resp.Body.Close()
		var data Pokemon
		err = json.NewDecoder(resp.Body).Decode(&data)
		if err != nil {
			fmt.Println("Error decoding data")
			return Pokemon{}, err
		}
		dataBytes, err := json.Marshal(data)
		if err != nil {
			fmt.Println("Error marshaling data")
			return Pokemon{}, err
		}
		c.CacheAdd(url, dataBytes)
		fmt.Println(data.Name)
		return data, nil
	}
}
