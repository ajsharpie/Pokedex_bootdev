type LocationAreasResponse struct {
	Count    int    `json:"count"`
	Next     string `json:"next"`
	Previous any    `json:"previous"`
	Results  []struct {
		Name string `json:"name"`
		URL  string `json:"url"`
	} `json:"results"`
}

func GetLocationAreas(url string, c *Cache) error {
	if result, ok := c.Get(url); ok {
		fmt.Println("Location Areas:")
		var cachedData LocationAreasResponse
		err := json.Unmarshal(result, &cachedData)
		if err != nil {
			fmt.Println("Error unmarshaling cached data")
			return err
		}
		for _, location := range cachedData.Results {
			fmt.Println(location.Name)
		}
		return nil
	} else {
		resp, err := http.Get(url)
		if err != nil {
			fmt.Println("Error fetching data")
			return err
		}
		defer resp.Body.Close()
		fmt.Println("Location Areas:")
		var data LocationAreasResponse
		err = json.NewDecoder(resp.Body).Decode(&data)
		if err != nil {
			fmt.Println("Error decoding data")
			return err
		}
		results := data.Results
		dataBytes, err := json.Marshal(data)
		if err != nil {
			fmt.Println("Error marshaling data")
			return err
		}
		c.CacheAdd(url, dataBytes)
		for _, result := range results {
			location := result.Name
			fmt.Println(location)
		}
		return nil
	}
}

